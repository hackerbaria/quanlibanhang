    private void loadView() throws SQLException{
    	List<NhomHang> rs = nhomHangService.getAll();
        DefaultTableModel model = new DefaultTableModel();
        String[] columnNames = {"ID", "Mã Nhóm Hàng", "Tên Loại Hàng", "Ghi Chú", "Còn Quản Lý"};
        model.setColumnIdentifiers(columnNames);
        jTable1.setModel(model);
        for(NhomHang nhomHang : rs){
            Integer id = nhomHang.getId();
            String maNhomHang = nhomHang.getMaNhomHang();
            String tenNhomHang = nhomHang.getTenNhomHang();
            String ghiChu = nhomHang.getGhiChu();
            conQuanLy = nhomHang.getConQuanLy();
            model.addRow(new Object[]{id, maNhomHang, tenNhomHang, ghiChu, conQuanLy});
        }
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jCheckBox1.setSelected(false);
        jScrollPane1.setViewportView(jTable1);
    }
    
