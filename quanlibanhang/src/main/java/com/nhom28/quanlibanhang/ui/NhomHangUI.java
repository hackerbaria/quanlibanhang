    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButton1ActionPerformed
    	maNhomHang = jTextField1.getText();
    	tenNhomHang = jTextField2.getText();
    	ghiChu = jTextField3.getText();
    	conQuanLy = (byte) (jCheckBox1.isSelected() ? 1 : 0);
    	nhomHang = new NhomHang(maNhomHang, tenNhomHang, ghiChu, conQuanLy);
    	nhomHangService.add(nhomHang);
    	loadView();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButton2ActionPerformed
    	int selectedRow = jTable1.getSelectedRow();
        Integer id = Integer.parseInt(jTable1.getModel().getValueAt(selectedRow, 0).toString());
        nhomHangService.delete(id);
        loadView();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButton3ActionPerformed
    	jTable1.setEnabled(true);
    	int selectedRow = jTable1.getSelectedRow();
        Integer id = Integer.parseInt(jTable1.getModel().getValueAt(selectedRow, 0).toString());
        maNhomHang = jTextField1.getText();
    	tenNhomHang = jTextField2.getText();
    	ghiChu = jTextField3.getText();
    	conQuanLy = (byte) (jCheckBox1.isSelected() ? 1 : 0);
    	nhomHangService.update(id, maNhomHang, tenNhomHang, ghiChu, conQuanLy);
        loadView();
    }//GEN-LAST:event_jButton3ActionPerformed

