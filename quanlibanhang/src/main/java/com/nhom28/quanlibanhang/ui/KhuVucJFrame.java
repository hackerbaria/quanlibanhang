	private void addNew(){		
		KhuVuc kv = new KhuVuc();
		kv.setMaKhuVuc(txtMaKhuVuc.getText());
		kv.setTenKhuVuc(txtTenKhuVuc.getText());
		kv.setGhiChu(txtGhiChu.getText());
		khuVucService.insertKhuVuc(kv);		
		
	}
	
	private void updateRecord() throws SQLException {
		KhuVuc entity = new KhuVuc();
		entity.setId(Integer.parseInt(txtId.getText()));
		entity.setMaKhuVuc(txtMaKhuVuc.getText());
		entity.setTenKhuVuc(txtTenKhuVuc.getText());
		entity.setGhiChu(txtGhiChu.getText());		
		khuVucService.updateKhuVuc(entity);	
    }
	
	private void loadRecords() throws SQLException {
		List<KhuVuc> khuVucList = khuVucService.getAllListKhuVuc();

		String colThucAn[] = { "ID", "Mã Đơn Vị", "Tên Đơn Vị", "Ghi Chú" };
		TableModel tableModel = new DefaultTableModel(colThucAn, 0) {
			@Override
			public boolean isCellEditable(int row, int column) {
				return true;
			}
		};
		jTable1.setModel(tableModel);

		// lay danh sach cach mon an
		for (int i = 0; i < khuVucList.size(); i++) {
			KhuVuc kv = khuVucList.get(i);
			int ma = kv.getId();
			String maKhuVuc = kv.getMaKhuVuc();
			String tenKhuVuc = kv.getTenKhuVuc();
			String ghiChu = kv.getGhiChu();

			Object[] data = { ma, maKhuVuc, tenKhuVuc, ghiChu };
			((DefaultTableModel) tableModel).addRow(data);
		}
    }

	
