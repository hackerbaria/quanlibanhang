    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
    	String message = "";
    	if(addRecord) {
    		message = "Bạn có muốn thêm mới Đơn Vị Tính Không?";
    	} else {
    		message = "Bạn có muốn cập nhật thông tin của đơn vị tính không?";
    	}
    	int dialogResult = JOptionPane.showConfirmDialog(null, message, "Xác Nhận?", JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {
            
        	try {
                if (addRecord == true) {
                    addNew();
                } else {
                    updateRecord();
                }

                addRecord = false;

                loadRecords();
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }
            
        }
    }//GEN-LAST:event_btnSuaActionPerformed

