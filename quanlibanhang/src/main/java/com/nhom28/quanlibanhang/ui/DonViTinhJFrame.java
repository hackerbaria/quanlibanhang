	private void addNew(){		
		
		DonViTinh entity = new DonViTinh();
		entity.setMaDonViTinh(jTextField2.getText());
		entity.setTenDonViTinh(jTextField3.getText());
		entity.setGhiChu(jTextField4.getText());
		
		donViService.insertDonViTinh(entity);
	}
	
	private void updateRecord() throws SQLException {
		DonViTinh entity = new DonViTinh();
		entity.setId(Integer.parseInt(jTextField1.getText()));
		entity.setMaDonViTinh(jTextField2.getText());
		entity.setTenDonViTinh(jTextField3.getText());
		entity.setGhiChu(jTextField4.getText());		
		donViService.updateDonViTinh(entity);		
    }
	
	private void loadRecords() throws SQLException {
		List<DonViTinh> donviTinhList = nguoiDung.getDonViTinhList();
        String DonViTinhColumn[] = {"ID", "Mã Đơn Vị", "Tên Đơn Vị"
                , "Ghi Chú"};
            TableModel tableModel = new DefaultTableModel(DonViTinhColumn, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return true;
                }
            };

        jTable1.setModel(tableModel);     
        for (int i = 0; i < donviTinhList.size(); i++) {
            int ma = donviTinhList.get(i).getId();
            String maDonVi = donviTinhList.get(i).getMaDonViTinh();
            String tenDonVi = donviTinhList.get(i).getTenDonViTinh();
            String ghiChu = donviTinhList.get(i).getGhiChu();

            Object[] data = { ma, maDonVi, tenDonVi, ghiChu};
            ((DefaultTableModel) tableModel).addRow(data);
        }
        
        
    }

	
