    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
    	int dialogResult = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn sửa thông tin nhà phân phối không?", "Xác Nhận?", JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {
            
        	try {
        		// lay thong tin nha phan phoi        		
                NhaPhanPhoi npp = new NhaPhanPhoi();
                npp.setId(Integer.parseInt(txtId.getText()));
                npp.setMaNhaPhanPhoi(txtMaNhaPhanPhoi.getText());
                
                Object item = jComboBox1.getSelectedItem();
                String key = ((ComboItem)item).getKey();                
                KhuVuc kv = new KhuVuc();
                
                               
                for (KhuVuc kc : khuVucList) {
					if(key.equalsIgnoreCase(String.valueOf(kc.getId()))) {
						kv = kc;
						break;
					}
				}
                
                npp.setKhuVuc(kv);
                npp.setTenNhaPhanPhoi(txtTenNhaPhanPhoi.getText());
                npp.setDiaChi(txtDiachi.getText());
                npp.setMaSoThue(txtMaSoThue.getText());
                npp.setDienThoai(txtDienThoai.getText());
                npp.setDiDong(txtDiDong.getText());
                npp.setEmail(txtEmail.getText());
                npp.setWebsite(txtWebsite.getText());
                npp.setSoTaiKhoan(txtSoTaiKhoan.getText());
                npp.setNganHang(txtNganHang.getText());
                npp.setFax(txtFax.getText());
                
                nhaPhanPhoi.update(npp);

                loadRecords();
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }
            
        }

    }//GEN-LAST:event_btnSuaActionPerformed

