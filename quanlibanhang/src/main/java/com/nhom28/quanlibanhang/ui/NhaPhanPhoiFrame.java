    private void loadRecords() throws SQLException {
		List<NhaPhanPhoi> nppList = nhaPhanPhoi.getAllNhaPhanPhoi();
        String titleColumn[] = {"ID", "Mã Nhà Phân Phối", "Khu Vực"
                , "Tên Nhà Phân Phối", "Địa Chỉ", "Mã Số Thuế",
                "Điện Thoại", "Di Động", "Email", "Website", "Số Tài Khoàn",
                "Ngân Hàng", "Fax"};
            TableModel tableModel = new DefaultTableModel(titleColumn, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return true;
                }
            };

        jTable1.setModel(tableModel);     
        for (int i = 0; i < nppList.size(); i++) {
        	NhaPhanPhoi npp = nppList.get(i);
            int id = npp.getId();
            String maNhaPhanPhoi = npp.getMaNhaPhanPhoi();
           
            int idkhuVuc = npp.getKhuVuc().getId();
            String tenKhuVuc = "";
            for (KhuVuc khuVuc : khuVucList) {
				if(khuVuc.getId() == idkhuVuc) {
					tenKhuVuc = khuVuc.getTenKhuVuc();					
				}
			}
           
            
            String TenNhaPhanPhoi = npp.getTenNhaPhanPhoi();
            String diaChi = npp.getDiaChi();
            String maSoThue = npp.getMaSoThue();
            String dienThoai = npp.getDienThoai();
            String diDong = npp.getDiDong();
            String email = npp.getEmail();
            String website = npp.getWebsite();
            String soTaiKhoan = npp.getSoTaiKhoan();
            String nganHang = npp.getNganHang();
            String fax = npp.getFax();
            

            Object[] data = { id, maNhaPhanPhoi, tenKhuVuc, TenNhaPhanPhoi,
            		diaChi, maSoThue, dienThoai, diDong, email, website, soTaiKhoan, nganHang,
            		fax};
            ((DefaultTableModel) tableModel).addRow(data);
        }
        
        
        
    }

